// ═══════════════════════════════════════════════════════
//  PDF CORE BUILDER
// ═══════════════════════════════════════════════════════
function generateCV(langCode) {
  if (!window.jspdf) { alert('PDF library not loaded.'); return; }

  var D   = CV_LANGS[langCode] || CV_LANGS['en'];
  var doc = new window.jspdf.jsPDF({ unit:'mm', format:'a4', compress:true });

  var PW=210, PH=297, SB_W=65;
  var MN_X=SB_W+7, MN_W=PW-SB_W-15;
  var SB_X=5, SB_TW=SB_W-10, HDR_H=30;

  var C = {
    sideBg:   [22,22,22],
    mainBg:   [250,250,250],
    hdrBg:    [10,10,10],
    accent:   [200,241,53],
    white:    [255,255,255],
    darkTxt:  [25,25,25],
    midTxt:   [85,85,85],
    muted:    [150,150,150],
    sideRule: [45,45,45],
    skillTxt: [200,200,200],
  };

  var y=0, sy=0;

  var fi = function(c){ doc.setFillColor(c[0],c[1],c[2]); };
  var ti = function(c){ doc.setTextColor(c[0],c[1],c[2]); };
  var di = function(c){ doc.setDrawColor(c[0],c[1],c[2]); };
  var fr = function(x,yy,w,h,c){ fi(c); doc.rect(x,yy,w,h,'F'); };

  function drawBg() {
    fr(0,0,PW,PH,C.mainBg);
    fr(0,0,SB_W,PH,C.sideBg);
    fr(SB_W,0,0.8,PH,C.accent);
    fr(0,0,PW,HDR_H,C.hdrBg);
    fr(0,HDR_H,PW,1.2,C.accent);
  }

  function sbHead(lbl) {
    sy+=5;
    doc.setFontSize(6.5); doc.setFont('helvetica','bold'); ti(C.accent);
    doc.text(lbl,SB_X,sy);
    sy+=1.5; di(C.sideRule); doc.setLineWidth(0.3);
    doc.line(SB_X,sy,SB_W-5,sy); sy+=4;
  }

  function mnHead(lbl) {
    y+=6;
    fr(MN_X-4,y-4,2,5,C.accent);
    doc.setFontSize(7.5); doc.setFont('helvetica','bold'); ti(C.darkTxt);
    doc.text(lbl,MN_X,y); y+=1;
    di([210,210,210]); doc.setLineWidth(0.3);
    doc.line(MN_X,y,MN_X+MN_W,y); y+=5;
  }

  function bul(text) {
    doc.setFontSize(8); doc.setFont('helvetica','normal'); ti(C.midTxt);
    var lines=doc.splitTextToSize(text,MN_W-6);
    doc.text('-',MN_X+1,y);
    lines.forEach(function(l,i){ doc.text(l,MN_X+5,y+i*3.8); });
    y+=lines.length*3.8+1;
  }

  function checkY(n){
    if(y+n>PH-12){ doc.addPage(); drawBg(); y=12; }
  }

  // ── Draw ────────────────────────────────────────
  drawBg();

  // Header
  doc.setFontSize(20); doc.setFont('helvetica','bold'); ti(C.white);
  doc.splitTextToSize(D.name,120).forEach(function(l,i){
    doc.text(l,SB_X,12+i*8);
  });
  doc.setFontSize(7.5); doc.setFont('helvetica','normal'); ti(C.accent);
  doc.splitTextToSize(D.role,130).forEach(function(l,i){
    doc.text(l,SB_X,22+i*4);
  });
  doc.setFontSize(7.5); ti(C.muted);
  [D.email,D.phone,D.location].forEach(function(v,i){
    doc.text(v,PW-6,11+i*5,{align:'right'});
  });

  y=HDR_H+9; sy=HDR_H+8;

  // Sidebar
  sbHead(D.L.personal);
  [[D.L.dob,D.dob],[D.L.nationality,D.nationality],['GitHub',D.github],['LinkedIn',D.linkedin]]
  .forEach(function(pair){
    doc.setFontSize(6.5); doc.setFont('helvetica','bold'); ti(C.muted);
    doc.text(pair[0],SB_X,sy); sy+=3.5;
    doc.setFontSize(7.5); doc.setFont('helvetica','normal'); ti(C.white);
    var ls=doc.splitTextToSize(pair[1],SB_TW);
    ls.forEach(function(l,i){ doc.text(l,SB_X,sy+i*3.6); });
    sy+=ls.length*3.6+2.5;
  });

  sbHead(D.L.languages);
  D.languages.forEach(function(lg){
    doc.setFontSize(8.5); doc.setFont('helvetica','bold'); ti(C.white);
    doc.text(lg.name,SB_X,sy); sy+=3.8;
    doc.setFontSize(7); doc.setFont('helvetica','normal'); ti(C.accent);
    doc.text(lg.level,SB_X,sy); sy+=4.5;
  });

  sbHead(D.L.skills);
  D.skills.forEach(function(s){
    doc.setFontSize(7); doc.setFont('helvetica','bold'); ti(C.accent);
    doc.text(s.cat,SB_X,sy); sy+=3.5;
    doc.setFontSize(7); doc.setFont('helvetica','normal'); ti(C.skillTxt);
    var ls=doc.splitTextToSize(s.detail,SB_TW);
    ls.forEach(function(l,i){ doc.text(l,SB_X,sy+i*3.4); });
    sy+=ls.length*3.4+2;
  });

  sbHead(D.L.interests);
  D.interests.forEach(function(item){
    doc.setFontSize(7.5); doc.setFont('helvetica','normal'); ti([190,190,190]);
    doc.text('- '+item,SB_X,sy); sy+=4;
  });

  // Main
  mnHead(D.L.profile);
  doc.setFontSize(8.5); doc.setFont('helvetica','normal'); ti(C.midTxt);
  doc.splitTextToSize(D.profile,MN_W).forEach(function(l){
    doc.text(l,MN_X,y); y+=4;
  }); y+=2;

  mnHead(D.L.experience);
  D.experience.forEach(function(job){
    checkY(28);
    doc.setFontSize(9.5); doc.setFont('helvetica','bold'); ti(C.darkTxt);
    doc.text(job.title,MN_X,y); y+=4.5;
    doc.setFontSize(8); doc.setFont('helvetica','normal'); ti(C.midTxt);
    var o=job.city ? job.org+', '+job.city : job.org;
    doc.splitTextToSize(o,MN_W).forEach(function(l){ doc.text(l,MN_X,y); y+=3.8; });
    fr(MN_X,y-2,1.5,1.5,C.accent);
    doc.setFontSize(7.5); doc.setFont('helvetica','italic'); ti(C.muted);
    doc.text(job.from+' — '+job.to,MN_X+3,y); y+=4.5;
    job.bullets.forEach(function(b){ checkY(8); bul(b); }); y+=3;
  });

  mnHead(D.L.education);
  D.education.forEach(function(edu){
    checkY(18);
    doc.setFontSize(9.5); doc.setFont('helvetica','bold'); ti(C.darkTxt);
    doc.splitTextToSize(edu.degree,MN_W).forEach(function(l){ doc.text(l,MN_X,y); y+=4.5; });
    doc.setFontSize(8); doc.setFont('helvetica','normal'); ti(C.midTxt);
    var o=edu.city ? edu.org+', '+edu.city : edu.org;
    doc.splitTextToSize(o,MN_W).forEach(function(l){ doc.text(l,MN_X,y); y+=3.8; });
    fr(MN_X,y-2,1.5,1.5,C.accent);
    doc.setFontSize(7.5); doc.setFont('helvetica','italic'); ti(C.muted);
    doc.text(edu.from ? edu.from+' — '+edu.to : edu.to, MN_X+3,y); y+=6;
  });

  mnHead(D.L.courses);
  D.courses.forEach(function(crs){
    checkY(28);
    doc.setFontSize(9.5); doc.setFont('helvetica','bold'); ti(C.darkTxt);
    doc.text(crs.title,MN_X,y); y+=4.5;
    doc.setFontSize(8); doc.setFont('helvetica','normal'); ti(C.midTxt);
    var o=crs.city ? crs.org+', '+crs.city : crs.org;
    doc.splitTextToSize(o,MN_W).forEach(function(l){ doc.text(l,MN_X,y); y+=3.8; });
    fr(MN_X,y-2,1.5,1.5,C.accent);
    doc.setFontSize(7.5); doc.setFont('helvetica','italic'); ti(C.muted);
    doc.text(crs.date,MN_X+3,y); y+=4.5;
    crs.bullets.forEach(function(b){ checkY(8); bul(b); }); y+=3;
  });

  doc.save('Mohammed_Salih_CV_'+langCode.toUpperCase()+'.pdf');
}

// ═══════════════════════════════════════════════════════
//  UI — Dropdown + Download button
// ═══════════════════════════════════════════════════════
document.addEventListener('DOMContentLoaded', function() {
  var cvBtn = document.getElementById('cv-download-btn');
  if (!cvBtn) return;

  var selectedLang = 'en';

  // ── Build dropdown ──────────────────────────────
  var wrapper = document.createElement('div');
  wrapper.className = 'cv-btn-wrapper';

  // Move cvBtn inside wrapper
  cvBtn.parentNode.insertBefore(wrapper, cvBtn);
  wrapper.appendChild(cvBtn);

  // Dropdown trigger (arrow button)
  var trigger = document.createElement('button');
  trigger.className   = 'cv-lang-trigger';
  trigger.type        = 'button';
  trigger.innerHTML   =
    '<span class="cv-lang-selected">EN</span>' +
    '<svg class="cv-lang-arrow" width="10" height="10" viewBox="0 0 10 10" fill="none">' +
    '<path d="M2 3.5L5 6.5L8 3.5" stroke="currentColor" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"/>' +
    '</svg>';
  trigger.setAttribute('aria-label','Select CV language');
  wrapper.appendChild(trigger);

  // Dropdown menu
  var menu = document.createElement('div');
  menu.className = 'cv-lang-menu';

  var langs = [
    { code:'en', label:'English',    note:'Full support' },
    { code:'de', label:'Deutsch',    note:'Full support' },
  ];

  var unsupported = [
    { code:'ar', label:'العربية',   note:'RTL — coming soon' },
    { code:'ko', label:'한국어',     note:'Font not supported' },
    { code:'ja', label:'日本語',     note:'Font not supported' },
    { code:'zh', label:'中文',       note:'Font not supported' },
  ];

  // Supported
  var supportedHead = document.createElement('div');
  supportedHead.className = 'cv-menu-group-label';
  supportedHead.textContent = 'Available';
  menu.appendChild(supportedHead);

  langs.forEach(function(l) {
    var item = document.createElement('button');
    item.className   = 'cv-menu-item';
    item.type        = 'button';
    item.dataset.lang = l.code;
    item.innerHTML   =
      '<span class="cv-menu-flag">'+l.code.toUpperCase()+'</span>' +
      '<span class="cv-menu-label">'+l.label+'</span>' +
      '<span class="cv-menu-note">'+l.note+'</span>';
    if (l.code === 'en') item.classList.add('active');
    item.addEventListener('click', function() {
      selectedLang = l.code;
      menu.querySelectorAll('.cv-menu-item').forEach(function(i){ i.classList.remove('active'); });
      item.classList.add('active');
      wrapper.querySelector('.cv-lang-selected').textContent = l.code.toUpperCase();
      closeMenu();
    });
    menu.appendChild(item);
  });

  // Unsupported
  var unsupportedHead = document.createElement('div');
  unsupportedHead.className = 'cv-menu-group-label cv-menu-group-label--dim';
  unsupportedHead.textContent = 'Not yet supported';
  menu.appendChild(unsupportedHead);

  unsupported.forEach(function(l) {
    var item = document.createElement('button');
    item.className   = 'cv-menu-item cv-menu-item--disabled';
    item.type        = 'button';
    item.disabled    = true;
    item.innerHTML   =
      '<span class="cv-menu-flag">'+l.code.toUpperCase()+'</span>' +
      '<span class="cv-menu-label">'+l.label+'</span>' +
      '<span class="cv-menu-note">'+l.note+'</span>';
    menu.appendChild(item);
  });

  wrapper.appendChild(menu);

  // ── Toggle menu ──────────────────────────────────
  var menuOpen = false;

  function openMenu() {
    menuOpen = true;
    menu.classList.add('open');
    trigger.classList.add('open');
  }

  function closeMenu() {
    menuOpen = false;
    menu.classList.remove('open');
    trigger.classList.remove('open');
  }

  trigger.addEventListener('click', function(e) {
    e.stopPropagation();
    menuOpen ? closeMenu() : openMenu();
  });

  document.addEventListener('click', function() { closeMenu(); });
  menu.addEventListener('click', function(e) { e.stopPropagation(); });

  // ── Download ─────────────────────────────────────
  cvBtn.addEventListener('click', function() {
    closeMenu();
    var label = cvBtn.querySelector('.cv-btn-label');
    var orig  = label.textContent;

    // Progress bar
    var bar = document.createElement('div');
    bar.className = 'cv-progress';
    wrapper.appendChild(bar);
    setTimeout(function(){ bar.style.width = '60%'; }, 50);

    label.textContent         = 'Generating...';
    cvBtn.style.pointerEvents = 'none';

    function doGenerate() {
      setTimeout(function() {
        try {
          bar.style.width = '100%';
          generateCV(selectedLang);
          label.textContent       = 'Downloaded ✓';
          cvBtn.style.borderColor = 'var(--accent)';
          setTimeout(function() {
            label.textContent         = orig;
            cvBtn.style.pointerEvents = '';
            cvBtn.style.borderColor   = '';
            if (bar.parentNode) bar.parentNode.removeChild(bar);
          }, 2500);
        } catch(e) {
          console.error('CV Error:', e);
          label.textContent = 'Error — try again';
          if (bar.parentNode) bar.parentNode.removeChild(bar);
          setTimeout(function() {
            label.textContent         = orig;
            cvBtn.style.pointerEvents = '';
          }, 2000);
        }
      }, 200);
    }

    if (window.jspdf) {
      doGenerate();
    } else {
      label.textContent = 'Loading...';
      var s = document.createElement('script');
      s.src = 'https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js';
      s.onload = doGenerate;
      s.onerror = function() {
        label.textContent = 'No internet';
        if (bar.parentNode) bar.parentNode.removeChild(bar);
        setTimeout(function() {
          label.textContent         = orig;
          cvBtn.style.pointerEvents = '';
        }, 3000);
      };
      document.body.appendChild(s);
    }
  });

});
